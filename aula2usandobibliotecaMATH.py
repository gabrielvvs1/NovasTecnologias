import math

print(round(2.9)) #arredondando numero
print(math.pi) #valor de pi
print(abs(-2.9)) #numero absoluto, sinal nao importa
print(pow(2,3)) #numero elevado
print(max(2,3)) #numero maximo
print(min(2,3)) #numero minimo
print(sum([1,2,3])) #soma de valores do vetor
print(chr(65)) #da o valor do numero em letra
print(ord('A')) #valor da letra em numero
print(divmod(10,3)) #valor da divisao e o resto
print(math.ceil(2.1)) #arredonda pra cima
print(math.floor(2.9)) #arredonda para baixo
